function filterEmployees(data, criteria) {
    let dataArr = JSON.parse(data);    
    criteria = criteria.split('-');
    critName  = criteria[0];
    critValue = criteria[1];
    if( !(critName === 'all') ) {    
    dataArr = dataArr.filter( el => el[critName] === critValue );
    }
    for ( i = 0 ; i < dataArr.length ; i ++ ) {
        console.log(`${i}. ${dataArr[i]['first_name']} ${dataArr[i]['last_name']} - ${dataArr[i]['email']}`);
    }
}

